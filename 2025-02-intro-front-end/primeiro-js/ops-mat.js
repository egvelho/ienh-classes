let numA = Number(prompt("Digite o número A"));
let numB = Number(prompt("Digite o número B"));

let resultadoSoma = numA + numB;
let resultadoSubtracao = numA - numB;
let resultadoMultiplicacao = numA * numB;
let resultadoDivisao = numA / numB;
let resultadoPotenciacao = numA ** numB;

alert("O resultado da soma é: " + resultadoSoma);
alert("O resultado da subtração é: " + resultadoSubtracao);
alert("O resultado da multiplicação é: " + resultadoMultiplicacao);
alert("O resultado da divisão é: " + resultadoDivisao);
alert("O resultado da potenciação é: " + resultadoPotenciacao);
