export function soma(numA: number, numB: number) {
  const resultado = numA + numB;
  return resultado;
}

export function subtrai(numA: number, numB: number) {
  const resultado = numA - numB;
  return resultado;
}

export function divide(numA: number, numB: number) {
  const resultado = numA / numB;
  return resultado;
}

export function multiplica(numA: number, numB: number) {
  const resultado = numA * numB;
  return resultado;
}
